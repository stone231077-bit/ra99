// Определение текущего языка (по умолчанию UA)
let currentLang = localStorage.getItem('lang') || 'uk';

// Функция перевода
function translatePage(lang) {
    currentLang = lang;
    localStorage.setItem('lang', lang);
    
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            // Для option элементов нужно обновлять value, а не textContent
            if (element.tagName === 'OPTION') {
                element.textContent = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });
    
    // Обновляем активную кнопку языка
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });
    
    // Обновляем атрибут lang у html
    if (document.documentElement) {
        document.documentElement.lang = lang;
    }
    const htmlLang = document.getElementById('htmlLang');
    if (htmlLang) {
        htmlLang.lang = lang;
    }
}

// Инициализация перевода при загрузке страницы
document.addEventListener('DOMContentLoaded', function() {
    translatePage(currentLang);
    
    // Обработчики для переключателя языков
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', function() {
            const lang = this.getAttribute('data-lang');
            translatePage(lang);
        });
    });
});

